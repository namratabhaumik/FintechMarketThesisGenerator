"""Offline eval for the thesis NARRATIVE (llm.summarize) quality.

  - the narrative is generated by Gemini and judged by qwen2.5:7b.
  - constrained verdicts (FAITHFUL/UNFAITHFUL, YES/PARTIAL/NO), parsed strictly.
  - before the real run, the faithfulness judge is shown a planted hallucination 
    and a clean paraphrase; if it cannot tell the apart, the scores are untrustworthy.

    dataset  -> a few (topic, fixture documents) pairs
    task     -> llm.summarize(documents) [Gemini] -> narrative
    scorers  -> faithful (judge), relevant (judge), non_degenerate (deterministic)

Run from the repo root:
    python -m evals.thesis_narrative_eval
"""

import os
from typing import List, Tuple

import requests
from dotenv import load_dotenv
from langchain_core.documents import Document
from langfuse import Evaluation, Langfuse
from langfuse.experiment import LocalExperimentItem

from config.settings import AppConfig
from dependency_injection.container import ServiceContainer

JUDGE_MODEL = os.getenv("JUDGE_MODEL", "qwen2.5:7b")
JUDGE_URL = os.getenv("OLLAMA_BASE_URL", "http://localhost:11434").rstrip("/") + \
    "/v1/chat/completions"

FIXTURES = {
    "embedded finance in Europe": [
        Document(page_content=(
            "Embedded finance lets non-banks offer payments and lending via APIs. "
            "BaaS providers in the EU report rising transaction volumes in 2025."
        )),
        Document(page_content=(
            "BNPL adoption grew across European retailers, though regulators have "
            "signalled tighter consumer-credit rules for installment products."
        )),
    ],
    "crypto exchanges": [
        Document(page_content=(
            "Major crypto exchanges expanded spot and derivatives trading pairs as "
            "on-chain volumes recovered through the year."
        )),
        Document(page_content=(
            "Several exchanges face ongoing regulatory scrutiny over custody and "
            "listing practices in multiple jurisdictions."
        )),
    ],
    "insurtech underwriting": [
        Document(page_content=(
            "Insurtechs increasingly use machine-learning models for underwriting "
            "and claims automation, citing improved loss ratios."
        )),
        Document(page_content=(
            "Incumbent insurers partner with or acquire insurtech startups to "
            "modernise pricing, while data-privacy concerns persist."
        )),
    ],
}


def _ollama_judge(instruction: str) -> str:
    """Send a single instruction to the local judge model, return its reply."""
    resp = requests.post(
        JUDGE_URL,
        json={
            "model": JUDGE_MODEL,
            "messages": [{"role": "user", "content": instruction}],
            "max_tokens": 120,
            "temperature": 0.0,
        },
        timeout=60,
    )
    resp.raise_for_status()
    return resp.json()["choices"][0]["message"]["content"] or ""


def judge_faithfulness(summary: str, docs_text: str) -> Tuple[float, str]:
    """1.0 if the judge finds the summary supported by the docs, else 0.0."""
    instruction = (
        "You check whether a SUMMARY is faithful to the source DOCUMENTS - i.e. it "
        "makes NO claim that the documents do not support.\n\n"
        f"DOCUMENTS:\n{docs_text}\n\nSUMMARY:\n{summary}\n\n"
        "Reply on the first line with exactly one word: FAITHFUL (every claim is "
        "supported) or UNFAITHFUL (at least one claim is not supported). Then give "
        "one short sentence of reasoning."
    )
    text = _ollama_judge(instruction).strip()
    upper = text.upper()
    # Check UNFAITHFUL first since it contains the substring FAITHFUL.
    score = 0.0 if "UNFAITHFUL" in upper else (1.0 if "FAITHFUL" in upper else 0.0)
    return score, text[:200]


def judge_relevance(summary: str, topic: str) -> Tuple[float, str]:
    """1.0 / 0.5 / 0.0 for how well the summary addresses the topic."""
    instruction = (
        "You check whether a SUMMARY addresses the TOPIC.\n\n"
        f"TOPIC: {topic}\n\nSUMMARY:\n{summary}\n\n"
        "Reply on the first line with exactly one word: YES (fully on-topic), "
        "PARTIAL, or NO. Then give one short sentence of reasoning."
    )
    text = _ollama_judge(instruction).strip()
    verdict = text.split()[0].upper().strip(".:,") if text else ""
    return {"YES": 1.0, "PARTIAL": 0.5, "NO": 0.0}.get(verdict, 0.0), text[:200]


def build_task(llm):
    """Task: summarize the topic's fixture documents with the real (Gemini) LLM."""

    def task(*, item, **kwargs) -> dict:
        topic = item["input"]["topic"]
        docs = FIXTURES[topic]
        summary = llm.summarize(docs)
        return {
            "summary": summary,
            "docs_text": "\n".join(d.page_content for d in docs),
        }

    return task


def faithful(*, input, output, expected_output=None, **kwargs) -> Evaluation:
    score, comment = judge_faithfulness(output["summary"], output["docs_text"])
    return Evaluation(name="faithful", value=score, data_type="NUMERIC", comment=comment)


def relevant(*, input, output, expected_output=None, **kwargs) -> Evaluation:
    score, comment = judge_relevance(output["summary"], input["topic"])
    return Evaluation(name="relevant", value=score, data_type="NUMERIC", comment=comment)


def non_degenerate(*, input, output, expected_output=None, **kwargs) -> Evaluation:
    """Deterministic gate: the narrative is non-empty and substantive."""
    words = len((output.get("summary") or "").split())
    return Evaluation(
        name="non_degenerate",
        value=words >= 20,
        data_type="BOOLEAN",
        comment=f"{words} words",
    )


def _mean(item_results, name: str) -> Evaluation:
    vals = [e.value for r in item_results for e in r.evaluations if e.name == name]
    return Evaluation(
        name=f"avg_{name}",
        value=sum(vals) / len(vals) if vals else 0.0,
        data_type="NUMERIC",
        comment=f"mean over {len(vals)}",
    )


def avg_faithful(*, item_results, **kwargs) -> Evaluation:
    return _mean(item_results, "faithful")


def avg_relevant(*, item_results, **kwargs) -> Evaluation:
    return _mean(item_results, "relevant")


def _negative_control() -> bool:
    """Confirm the faithfulness judge can tell a hallucination from a clean summary.

    Returns True if it discriminates (planted-bad scores lower than clean-good and
    is not marked faithful). If False, faithfulness scores are not trustworthy.
    """
    docs = FIXTURES["embedded finance in Europe"]
    docs_text = "\n".join(d.page_content for d in docs)
    clean = (
        "Embedded finance lets non-banks offer payments and lending via APIs, and "
        "BNPL adoption is growing in Europe amid tighter consumer-credit rules."
    )
    planted = clean + " Stripe also acquired Visa for $900 billion last week."  # not in docs
    good, _ = judge_faithfulness(clean, docs_text)
    bad, _ = judge_faithfulness(planted, docs_text)
    discriminates = bad < good and bad < 1.0
    print(f"Negative control: clean={good:.1f} planted_hallucination={bad:.1f} -> "
          f"{'DISCRIMINATES' if discriminates else 'FAILED (scores unreliable)'}")
    return discriminates


def _print_summary(result) -> None:
    print("\nPer-item results:")
    for r in result.item_results:
        topic = r.item["input"]["topic"]
        by = {e.name: e for e in r.evaluations}
        fv = by["faithful"].value if "faithful" in by else 0.0
        rv = by["relevant"].value if "relevant" in by else 0.0
        ndok = "ok" if by.get("non_degenerate") and by["non_degenerate"].value else "XX"
        print(f"  {topic[:34]:<34} faithful={fv:.1f} relevant={rv:.1f} "
              f"non_degenerate={ndok}")
        if "faithful" in by:
            print(f"      judge(faithful): {by['faithful'].comment[:90]}")
    for ev in result.run_evaluations:
        print(f"\n  {ev.name}: {ev.value:.3f}")
    url = getattr(result, "dataset_run_url", None)
    if url:
        print(f"\nLangfuse: {url}")


def main() -> None:
    load_dotenv()
    config = AppConfig.from_env()
    llm = ServiceContainer(config).get_llm()
    print(f"Generator: {config.llm.model_name} | Judge: {JUDGE_MODEL} (cross-family)\n")

    if not _negative_control():
        print("WARNING: judge did not discriminate - faithfulness scores below are "
              "untrustworthy. Investigate the judge before relying on them.\n")

    data: List[LocalExperimentItem] = [
        {"input": {"topic": topic}} for topic in FIXTURES
    ]

    langfuse = Langfuse()
    result = langfuse.run_experiment(
        name="thesis-narrative-quality",
        description=f"Faithfulness + relevance, {config.llm.model_name} judged by {JUDGE_MODEL}",
        data=data,
        task=build_task(llm),
        evaluators=[faithful, relevant, non_degenerate],
        run_evaluators=[avg_faithful, avg_relevant],
    )
    langfuse.flush()
    _print_summary(result)


if __name__ == "__main__":
    main()